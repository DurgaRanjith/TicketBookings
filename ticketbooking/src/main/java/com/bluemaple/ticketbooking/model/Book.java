package com.bluemaple.ticketbooking.model;

public class Book {
	private String timings;
	
	
	public Book(){};
	
	
	

	public Book(String timings) {
		
		this.timings = timings;
	}

	public String getTimings() {
		return timings;
	}

	public void setTimings(String timings) {
		this.timings = timings;
	}
	
	

}
